import { z } from "zod";

const ALPHABET = [];

const GridCharacter = z
  .enum([
    "a",
    "b",
    "c",
    "d",
    "e",
    "f",
    "g",
    "h",
    "i",
    "j",
    "k",
    "l",
    "m",
    "n",
    "o",
    "p",
    "q",
    "r",
    "s",
    "t",
    "u",
    "v",
    "w",
    "x",
    "y",
    "z",
  ])
  .refine(
    (val) =>
      [
        "a",
        "b",
        "c",
        "d",
        "e",
        "f",
        "g",
        "h",
        "i",
        "j",
        "k",
        "l",
        "m",
        "n",
        "o",
        "p",
        "q",
        "r",
        "s",
        "t",
        "u",
        "v",
        "w",
        "x",
        "y",
        "z",
      ].includes(val),
    {
      message: "Invalid character. The value must be a lowercase letter [a-z]",
    }
  )
  .optional();

const getGridSchema = z.object({
  bias: GridCharacter,
});

export { getGridSchema };
